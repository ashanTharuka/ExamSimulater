    private void veryPassTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_veryPassTextKeyReleased
        char[] password1 = passText.getPassword();
        char[] password2 = veryPassText.getPassword();
        String pass1 = String.valueOf(password1);
        String pass2 = String.valueOf(password2);
        if (pass1.equals(pass2)) {
            w2.setVisible(false);
            updateButton.setVisible(true);

        } else {
            w2.setVisible(true);
            updateButton.setVisible(false);
        }
    }//GEN-LAST:event_veryPassTextKeyReleased

    private void updateButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateButtonMouseClicked
        try {
            String name = nameText.getText();
            String code = idText.getText();
            String number = numberText.getText();
            String email = emailText.getText();
            char[] password = veryPassText.getPassword();
            String pass = String.valueOf(password);
            Admin admin = new Admin(code, name, number, pass, email);

            try {
                AdminController ac = ServerConnecter.getServerConnecter().getAdminController();
                boolean isTrue = ac.updateAdmin(admin);
                if (isTrue) {
                    idText.setText(null);
                    nameText.setText(null);
                    numberText.setText(null);
                    oldPassText.setText(null);
                    passText.setText(null);
                    veryPassText.setText(null);
                    emailText.setText(null);
                    JOptionPane.showMessageDialog(null, "Update Successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Update Fail");
                }
            } catch (NotBoundException | MalformedURLException | RemoteException ex) {
                Logger.getLogger(Update_Admin_Panal.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException | IOException ex) {
                Logger.getLogger(Update_Admin_Panal.class.getName()).log(Level.SEVERE, null, ex);
            }
        } finally {
            try {
                AdminController adminController = ServerConnecter.getServerConnecter().getAdminController();
                adminController.releaseAdmin(this.code);
            } catch (NotBoundException | MalformedURLException | RemoteException ex) {
                Logger.getLogger(Delete_Admin_Panal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_updateButtonMouseClicked

