    private void idTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idTextActionPerformed
        try {
            String code = idText.getText();
            relese.code = code;
            AdminController adminController = ServerConnecter.getServerConnecter().getAdminController();

            if (this.code != code) {
                adminController.releaseAdmin(this.code);
            } else {
                this.code = code;
            }

            admin = adminController.SearchAdmin("id", code);
            if (admin == null) {
                JOptionPane.showMessageDialog(null, "Not Match");

            } else {
                if (adminController.reserveAdmin(code)) {
                    nameText.setText(admin.getName());
                    numberText.setText(admin.getPhoneNumber());
                    emailText.setText(admin.getEmail());
                } else {
                    JOptionPane.showMessageDialog(this, code + " is reserved");
                }

            }
        } catch (NotBoundException | MalformedURLException | RemoteException ex) {
            Logger.getLogger(Delete_Admin_Panal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Delete_Admin_Panal.class.getName()).log(Level.SEVERE, null, ex);
        }              // TODO add your handling code here:
    }//GEN-LAST:event_idTextActionPerformed

