    private void oldPasswordTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oldPasswordTextActionPerformed
        String oldPass = String.valueOf(oldPasswordText.getPassword());
        String[] email = null;
        try {
            AdminController ac = ServerConnecter.getServerConnecter().getAdminController();
            try {
                email = ac.getInstitueEmailPassword().split(":");
            } catch (NullPointerException ex) {
            }
            if (email == null) {
                JOptionPane.showMessageDialog(null, "Emty");
            } else {
                if (email[1].equals(oldPass)) {
                    oldPasswordText.setText(null);
                    emailLabel.setVisible(false);
                    oldPasswordText.setVisible(false);
                    ePanel.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Wrong Password", "Warning", 2);
                    oldPasswordText.setText(null);
                }
            }
        } catch (NotBoundException | MalformedURLException | RemoteException ex) {
            Logger.getLogger(EmailPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(EmailPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_oldPasswordTextActionPerformed

    private void doneButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_doneButtonMouseClicked
        String email = emailText.getText();
        String password = String.valueOf(newPasswordText.getPassword());

        if (emailText.getText().isEmpty() || emailText.getText() == null &&String.valueOf( newPasswordText.getPassword()) == null||String.valueOf( newPasswordText.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fill data !!!");
        } else {
            try {
                AdminController ac = ServerConnecter.getServerConnecter().getAdminController();
                boolean updateInstituteEmail = ac.updateInstituteEmail(email, password);
                if (updateInstituteEmail) {
                    JOptionPane.showMessageDialog(null, "Update Successfully ");
                    emailText.setText(null);
                    newPasswordText.setText(null);

                } else {
                    JOptionPane.showMessageDialog(null, "Update Fail ");
                }
            } catch (NotBoundException ex) {
                Logger.getLogger(EmailPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (MalformedURLException ex) {
                Logger.getLogger(EmailPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (RemoteException ex) {
                Logger.getLogger(EmailPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(EmailPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(EmailPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_doneButtonMouseClicked

